<h1>What We Do</h1>

<p>Our work concerns the development and study of precise mathematical formalisms that can be employed for the design, specification,
   and verification of computer programs. This is in a tradition of mathematical semantics that was started in the UK in the 1960s
   by Peter Landin, Christopher Strachey, and Dana Scott. This kind of semantics encompasses many mathematical theories,
   foremost among them order theory, topology, logic, game theory, and category theory, all of which feature in the work
   of the Birmingham Theory Group. It is the rich and often surprising interplay between these mathematical areas and programming
   concerns that gives rise to opportunities for deep and profound research.
</p>

<p>Recent work has included the following topics:
<ul>
<li>linear logic and continuation semantics: Berdine, O'Hearn, Reddy, Thielecke: <b>Linear Continuation Passing</b>. <em>Higher Order and Symbolic Computation</em>, vol.&nbsp;15, pages 181-208, 2002. 
<li>verification of concurrent imperative programs: Reddy, Reynolds: <b>Syntactic Control of Interference for Separation Logic</b>. <em>ACM Symp. on Principles of Programming Languages</em>, 2012. 
<li>compilation of functional programs into hardware: Ghica, Smith: <b>Geometry of Synthesis III: Resource management through type inference</b>. <em>38th Symposium on Principles of Programming Languages</em>, 2011.
<li>application of process calculi to security: Arapinis, Chothia, Ritter and Ryan: Analysing Unlinkability and Anonymity Using the Applied Pi Calculus. Computer Security Foundations Symposium 2010 (CSF'10), pp. 107-121.
<li>extensions to Stone duality: Jung, Moshier: <b>On the bitopological nature of Stone Duality</b>. Technical Report CSR-06-13. School of Computer Science, University of Birmingham, 2006, 110 pages.
<li>applications of geometric logic to topos approaches to quantum theory:
Fauser, Raynaud, Vickers: The Born rule as structure of spectral
bundles. 8th International Workshop on Quantum Physics and Logic, 2011.
<li>direct topological arguments for proving higher-order programs correct: Escardo, Ho: <b>Operational domain theory and topology of a sequential programming language</b>.  <em>20th Annual IEEE Symposium on Logic in Computer Science</em>, 2005.
					       <li>universal constructions on monads: Bowler, Goncharov, Levy, Schr&ouml;der: <b>Exploring the boundaries of monad tensorability on Set</b>. </em>Logical Methods in Computer Science 9(3)</em>, 2013.
					       <li>the relationship between temporal logic and interactive programming: Krishnaswami: <b>Higher-Order Reactive Programming without Spacetime Leaks</b>. <em>18th ACM SIGPLAN									       International Conference on Functional Programming</em>, 2013.
</ul>

<p>One of the most fascinating aspects of our work is that the mathematical theories we explore have found interest and application beyond the realm of semantics. A prime example of this is the link between type theory and homotopy theory which shows promise to provide a new constructive foundation for mathematics. Similarly, work in abstract category theory, originally developed with applications in computing in mind, is now being deployed for a new foundation of quantum physics.</p>

<p>Members of the Birmingham Theory Group have written many of the widely-cited fundamental texts that expound the core techniques and principles of mathematical semantics, for example
<ul>
<LI>Samson Abramsky and Achim Jung: <b>Domain Theory</b>.
    In <EM>Handbook of Logic in Computer Science</EM>, vol.&nbsp;III,
    Clarendon Press, 1994, pages 1-168.

<li>Steve Vickers: <b>Topology via Logic</b>.
    <em>Cambridge Tracts in Theoretical Computer Science</em>,
    vol.&nbsp5, Cambridge University Press, 1989.

<li>Martin Escardo: <b>Synthetic topology of data types and classical
    spaces</b>. In <em>Domain-theoretic Methods in Probabilistic
    Processes</em>, vol.&nbsp87, Electronic Notes in Theoretical
    Computer Science, 2004.

<li>Paul Levy: <b>Call-by-push-value. A functional/imperative synthesis</b>. <em>Semantic Structures in Computation</em>, Springer 2004. 

<li>Uday Reddy: <b>Global state considered unnecessary: An introduction to object-based semantics</b>. <em>Journal on Lisp and Symbolic Computation</em> vol.&nbsp;9, 1996, pages 7-76.

<li>David Pym and Eike Ritter: <b>Reductive Logic and Proof-search: Proof Theory, Semantics, and Control</b>. <em>Oxford Logic Guides</em>, Oxford University Press, 2004.
</ul>
</p>

<p>We are co-founders of the <a href="http://www.cs.nott.ac.uk/MGS/">Midlands Graduate School in the Foundations of Computing Science</a> which has introduced more than 400 PhD students and researchers to foundational and advanced topics in mathematical semantics, and have participated in many similar events worldwide.</p>

<p>The Group runs twice-weekly lab sessions and a weekly seminar series with speakers from the UK and beyond.</p>

